using System;

namespace CommonMethods
{
    public class Calculations
    { 
        public int AddNumbers(int Number1, int Number2)
        { 
            //Processing
            int result = Number1 + Number2;
            //return value
            return result;
        }

        public int MultipleNumbers(int Number1, int Number2)
        {
            //Processing
            int result = Number1 * Number2;
